namespace HealthInvoice.Core.Interfaces.Services.Authorization;

/// <summary>
/// Сервис для хеширования и верификации паролей.
/// Предоставляет методы для создания хеша пароля и проверки соответствия введённого пароля сохранённому хешу.
/// Используется для безопасной работы с паролями — предотвращает хранение паролей в открытом виде.
/// </summary>
public interface IPasswordHasherService
{
    /// <summary>
    /// Создаёт хеш для указанного пароля.
    /// Метод генерирует случайную соль, вычисляет хеш пароля с использованием криптографической функции
    /// и возвращает строку, содержащую соль и хеш, разделённые символом ':'.
    /// </summary>
    /// <param name="password">Пароль в виде открытого текста, который необходимо хешировать.</param>
    /// <returns>Строка, содержащая соль и хеш пароля, разделённые символом ':'.</returns>
    string HashPassword(string password);

    /// <summary>
    /// Проверяет, соответствует ли введённый пароль сохранённому хешу.
    /// Метод разделяет сохранённый хеш на соль и сам хеш, вычисляет хеш для введённого пароля
    /// с использованием той же соли и сравнивает результаты на равенство.
    /// Использует безопасное сравнение строк (fixed-time comparison) для защиты от тайминг-атак.
    /// </summary>
    /// <param name="password">Пароль в виде открытого текста для проверки.</param>
    /// <param name="storedHash">Сохранённый хеш пароля (строка в формате 'salt:hash').</param>
    /// <returns>
    /// true, если введённый пароль соответствует сохранённому хешу;
    /// false — в противном случае.
    /// </returns>
    bool VerifyPassword(string password, string storedHash);
}